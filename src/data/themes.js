// Helper function to get random items from an array
export function getRandomItems(array, count) {
  const shuffled = [...array].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}

// Helper function to get SVG path
export function getSVGPath(themeId, iconName) {
  // Handle core icons
  if (themeId === 'core') {
    return `/themes/core/${iconName}`
  }
  
  const theme = THEMES.find(t => t.id === themeId)
  if (!theme) return ''
  // For Supabase hosting, you'll need to adjust this path
  // This assumes SVGs are in a public/themes folder
  return `/themes/${theme.folder}/${iconName}`
}

